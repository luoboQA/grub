; ==================================================
; 16位引导扇区程序（Boot Sector）
; 功能：在启动时显示 "Hello world!"
; 编译：nasm -f bin boot.asm -o boot.bin
; 运行：qemu-system-x86_64 boot.bin
; ==================================================

bits 16                      ; 告诉 NASM 生成16位代码（实模式）
org 0x7c00                   ; 告诉 NASM 代码将被加载到内存地址 0x7c00
                             ; BIOS 会将引导扇区加载到 0x7c00 地址

boot:
    mov si, hello            ; SI 寄存器指向 hello 字符串的地址
    mov ah, 0x0e             ; AH = 0x0e（BIOS 功能号：TTY 模式写字符）
                             ; 使用 BIOS 中断 0x10 的写字符功能

.loop:
    lodsb                    ; 加载字符串字节到 AL 并自动递增 SI
                             ; 等价于：mov al, [si]; inc si
    
    or al, al                ; 检查 AL 是否为 0（字符串结束符）
                             ; OR 指令会影响 ZF 标志位
    jz halt                  ; 如果 ZF=1（AL=0），跳转到 halt 标签

    int 0x10                 ; 调用 BIOS 中断 0x10（视频服务）
                             ; 功能号 AH=0x0e：在屏幕上显示 AL 中的字符
                             
    jmp .loop                ; 跳回 .loop 继续处理下一个字符

halt:
    cli                      ; 清除中断标志位（Clear Interrupts）
                             ; 禁止外部中断，防止 CPU 被干扰
    hlt                      ; 暂停 CPU 执行（Halt）
                             ; 直到下一次中断或复位才会继续

hello: 
    db "Hello world!", 0     ; 定义要显示的字符串，末尾加 0 作为结束符

; ==================================================
; 引导扇区填充
; 一个引导扇区必须是 512 字节，最后两个字节必须是 0x55 0xAA
; ==================================================
times 510 - ($ - $$) db 0    ; 填充 0 直到第 510 字节
                             ; $   = 当前位置地址
                             ; $$  = 当前段起始地址（0x7c00）
                             ; 计算已使用的字节数，用 0 填充剩余空间
                             
dw 0xaa55                    ; 最后两个字节（511 和 512）必须是 0x55 0xAA
                             ; 这是引导扇区的魔数，BIOS 验证后才会启动
                             ; dw 定义双字节（word），小端序存储 0x55 0xAA